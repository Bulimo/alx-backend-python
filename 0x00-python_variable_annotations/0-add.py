#!/usr/bin/env python3
""" Module 0-add """


def add(a: float, b: float) -> float:
    """
    Adds two floats and returns their sum.

    Parameters:
    - a (float): The first float operand.
    - b (float): The second float operand.

    Returns:
    float: The sum of a and b.
    """
    return a + b
